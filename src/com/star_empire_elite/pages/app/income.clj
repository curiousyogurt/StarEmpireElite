;;;;;
;;;;; Income Phase - Resource Generation
;;;;;
;;;;; The income phase is the first phase of each turn where players collect resources from their
;;;;; planets and any other sources. Unlike later phases (expenses, exchange, building), the income
;;;;; phase requires no player decisions; it's purely informational, showing what resources will be
;;;;; generated before the player advances to the next phase.
;;;;;

(ns com.star-empire-elite.pages.app.income
  (:require [com.biffweb :as biff]
            [com.star-empire-elite.ui :as ui]
            [com.star-empire-elite.utils :as utils]))

;;;;
;;;; Calculations
;;;;

(defn calculate-income
  "Calculate income from all sources using game constants.

  [player game] -> {:ore-credits int, :ore-fuel int,
                    :food-food int, :mil-soldiers int, :mil-fighters int,
                    :mil-stations int, :tax-credits int}"
  [player game]
  ;; Keys such as :player/ore-planets are fully qualified keys in the player map.
  ;; That is, there is a key in the player map named :player/ore-planets, etc.
  ;;             (* resource-count-for-player     resource-value-in-game)
  {:ore-credits  (* (:player/ore-planets player)  (:game/ore-planet-credits game))
   :ore-fuel     (* (:player/ore-planets player)  (:game/ore-planet-fuel game))
   :food-food    (* (:player/food-planets player) (:game/food-planet-food game))
   :mil-soldiers (* (:player/mil-planets player)  (:game/mil-planet-soldiers game))
   :mil-fighters (* (:player/mil-planets player)  (:game/mil-planet-fighters game))
   :mil-stations (* (:player/mil-planets player)  (:game/mil-planet-stations game))
   :tax-credits  (* (:player/population player)   (:game/population-tax-credits game))})

;;;;
;;;; UI Components
;;;;

(defn income-row
  "Renders an income row showing resources generated by a planet type.

  Uses custom mobile/desktop layouts optimized for income display:
  - Mobile:  Compact grid showing planet type, count, and non-zero resources only
  - Desktop: Full table row with all columns, zero values dimmed at 20% opacity

  [planet-type-name str, planet-count int, income-map {:credits? int, :fuel? int, ...}] -> hiccup"
  [planet-type-name planet-count income-map]
  [:div.border-b.border-green-400.last:border-b-0

   ;; Mobile: Compact grid with narrower planet type column
   [:div.grid.gap-1.px-2.py-2.items-center.text-xs.leading-tight.lg:hidden
    {:style {:grid-template-columns "0.5fr 0.4fr 4fr"}}  ; Reduced first two columns, expanded last

    ;; Planet type
    [:div.font-mono planet-type-name]

    ;; Planet count (formatted with parentheses)
    [:div.text.font-mono "(" (ui/format-number planet-count) ")"]

    ;; Resources - all in one column, spaced out
    [:div.flex.gap-3.justify-start.flex-wrap
     (for [[k label] [[:credits "Crds"] [:fuel "Fuel"]
                      [:food "Food"] [:soldiers "Sold"] [:fighters "Fghts"]
                      [:stations "Stns"]]
           :let [v (or (k income-map) 0)]
           :when (pos? v)]
       [:div.whitespace-nowrap {:key k}
        [:span.text-green-300 label]
        [:span.font-mono.ml-1 "+" (ui/format-number v)]])]]

   ;; Desktop: Full table row with formatted planet count
   [:div.hidden.lg:grid.lg:gap-4.lg:items-center.lg:px-4.lg:py-3
    {:style {:grid-template-columns "repeat(8, minmax(0, 1fr))"}}
    [:div planet-type-name]
    [:div (ui/format-number planet-count)]
    (for [k [:credits :fuel :food :soldiers :fighters :stations]
          :let [v (or (k income-map) 0)]]
      [:div {:key k}
       [:span.font-mono {:style {:word-spacing "-0.2em"}
                         :class (when (zero? v) "opacity-20")}
        "+" (ui/format-number v)]])]])

